public class bai7_pheptoancoban {
    public static void main(String[] args) {
        int a = 17, b =5;
        System.out.println("a = "+a);
        System.out.println("b = "+b);

        //cộng
        System.out.println("a+b: " +(a+b));

        //trừ
        System.out.println("a-b: " +(a-b));

        //nhân
        System.out.println("a*b: " +(a*b));

        //chia lấy dư
        System.out.println("a%b: " +(a%b));

        //chia
        System.out.println("a/b: " +((float)a/b)); //Lưu ý chỗ phép chia!
    }
}
